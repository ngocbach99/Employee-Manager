    private void TinhLuong_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TinhLuong_ActionPerformed
        // TODO add your handling code here:
        //lay du lieu tu ngay nao do
        String Tu_ngay = String.valueOf(Tu_N.getSelectedItem().toString().trim());
        String Tu_thang = String.valueOf(Tu_T.getSelectedItem().toString().trim());
        String Tu_nam = String.valueOf(Tu_Nam.getSelectedItem().toString().trim());
        //den ngay nao do
        String Den_ngay = String.valueOf(Den_N.getSelectedItem().toString().trim());
        String Den_thang = String.valueOf(Den_T.getSelectedItem().toString().trim());
        String Den_nam = String.valueOf(Den_Nam.getSelectedItem().toString().trim());
        //dinh dang ngay;
        String NgayThang_TU = Tu_nam + "-" + Tu_thang + "-" + Tu_ngay;
        String NgayThang_DEN = Den_nam + "-" + Den_thang + "-" + Den_ngay;

        int kiemtra_tungay_nhohon_denngay = NgayThang_TU.compareTo(NgayThang_DEN);

        boolean chekcdateTu = SauNgayHienTai(Integer.parseInt(Tu_ngay), Integer.parseInt(Tu_thang), Integer.parseInt(Tu_nam));
        boolean chekcdateDen = SauNgayHienTai(Integer.parseInt(Den_ngay), Integer.parseInt(Den_thang), Integer.parseInt(Den_nam));
        boolean hopleDateTu = laNgayHopLe(Integer.parseInt(Tu_ngay), Integer.parseInt(Tu_thang), Integer.parseInt(Tu_nam));
        boolean hopleDateDen = laNgayHopLe(Integer.parseInt(Den_ngay), Integer.parseInt(Den_thang), Integer.parseInt(Den_nam));

        if (hopleDateTu == false) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Tính lương không hợp lê", "Erro", 1);
        } else if (hopleDateDen == false) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Tính lương không hợp lê", "Erro", 1);
        } else if (kiemtra_tungay_nhohon_denngay > 0) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Tính lương không hợp lê (Ngày bắt đầu tính phải lớn hơn đến ngày tính)", "Erro", 1);
        } else if (chekcdateTu == false) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Tính lương không hợp lê (Các Ngày Phải Sau Ngày Hiện Tại)", "Erro", 1);
        } else if (chekcdateDen == false) {
            JOptionPane.showMessageDialog(rootPane, "Ngày Tính lương không hợp lê (Các Ngày Phải Sau Ngày Hiện Tại)", "Erro", 1);
        }
        try {
            Connection conn = connection.ConnectMySQL();
            CallableStatement cStmt = (CallableStatement) conn.prepareCall("{call tinhluongnhanvien(?,?)}");
            cStmt.setString(1, NgayThang_TU);
            cStmt.setString(2,NgayThang_DEN);
            ResultSet rs = cStmt.executeQuery();
            tableModel = (DefaultTableModel) Table_LuongNV.getModel();
            tableModel.setRowCount(0);
            while (rs.next()) {
                String manv = rs.getString("manv");
                String tennv = rs.getString("tennv");
                String cv = rs.getString("cv");
                String dongia = rs.getString("dongia");
                String sc = rs.getString("soca");
                String luong = rs.getString("luong");
                tableModel = (DefaultTableModel) Table_LuongNV.getModel();
                tableModel.addRow(new String[]{manv, tennv, cv, dongia, sc, luong});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_TinhLuong_ActionPerformed

