    private void jButtonPBNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPBNActionPerformed

        DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
        Connection conn = (Connection) connection.ConnectMySQL();
        //lay du lieu tu bang
        String ma_cv = String.valueOf(jTextFieldMPB.getText()).trim();
        String ten_cv = String.valueOf(jTextFieldTPB.getText().trim());
        String hesoluong = String.valueOf(jTextFieldLPB.getText()).trim();
        String ma_nv = String.valueOf(jTextField2.getText()).trim();

        /*
        Kiểm tra xem các trường có trống hay không nếu có thì thông báo lỗi.
        */
        if (ma_cv.equals("") || ten_cv.equals("") || hesoluong.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Yêu cầu nhập đầy đủ thông tin", "Erro", 1);
        }else {
            //Thêm thông tin vào database.
            try {
                CallableStatement cStmt = (CallableStatement) conn.prepareCall("{call kiemtratontaichucvu(?)}");
                cStmt.setString(1, ma_cv);
                ResultSet rs_checkid = cStmt.executeQuery();
                while (rs_checkid.next()) {
                    String ma = rs_checkid.getString("ma_cv");
                    if (ma.equals(ma_cv)) {
                        JOptionPane.showMessageDialog(rootPane, "Mã phòng ban đã tồn tại! Yêu cầu nhập mã lại", "Erro", 1);
                    }
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            try {
                CallableStatement cStmt = (CallableStatement) conn.prepareCall("{call themchucvu(?,?,?,?)}");
                cStmt.setString(1,ma_cv);
                cStmt.setString(2, ten_cv);
                cStmt.setInt(3, Integer.parseInt(hesoluong));
                cStmt.setString(4, ma_nv);
                ResultSet rs = cStmt.executeQuery();
                tableModel.addRow(new String[]{ma_cv, ten_cv, hesoluong});
                HienThiPB();
                hienthiPhongBan();
                HienThiTTNV();
                initCombobox();
                reset();
            } catch (Exception e) {
                e.getStackTrace();
            }
        }

    }//GEN-LAST:event_jButtonPBNActionPerformed

