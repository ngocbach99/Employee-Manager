    //Tính số ngày chọn trong tháng
    int tinhSoNgay(int thang, int nam) {
        int songay = 0;
        switch (thang) {
            case 1:
            case 3:
            case 5:
            case 7:
            case 8:
            case 10:
            case 12:
                songay = 31;
                break;
            case 4:
            case 6:
            case 9:
            case 11:
                songay = 30;
                break;
            case 2: {
                if (laNamNhuan(nam)) {
                    songay = 29;
                } else {
                    songay = 28;
                }
                break;
            }
        }
        return songay;
    }
    
    // Kiểm tra ngày nhập vào có hợp lệ hay không?
    private boolean laNgayHopLe(int nDay, int nMonth, int nYear) {
        // Kiểm tra năm
        if (nYear < 0) {
            return false; // Ngày không còn hợp lệ nữa!
        }
        // Kiểm tra tháng
        if (nMonth < 1 || nMonth > 12) {
            return false; // Ngày không còn hợp lệ nữa!
        }
        // Kiểm tra ngày
        if (nDay < 1 || nDay > tinhSoNgay(nMonth, nYear)) {
            return false; // Ngày không còn hợp lệ nữa!
        }
        return true; // Trả về trạng thái cuối cùng...
    }

